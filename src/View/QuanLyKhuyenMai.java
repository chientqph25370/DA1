    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        QLKhuyenMaiReponse km = getData();
        if (txtMaKM.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập mã khuyến mãi", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        } else if (txtTenKM.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tên khuyến mãi");
        } else {
            service.insert(km);
            loadData();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        QLKhuyenMaiReponse reponses = new QLKhuyenMaiReponse();
        reponses.setMaKm(ma);
        service.delete(reponses);
        loadData();
        txtMaKM.setText("");
        txtNgayBatDau.setText("");
        txtNgayKetThuc.setText("");
        txtNoiDung.setText("");
        txtTenKM.setText("");
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbKhuyenMaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbKhuyenMaiMouseClicked
        int row = tbKhuyenMai.getSelectedRow();
        ma = tbKhuyenMai.getModel().getValueAt(row, 0).toString();
        txtMaKM.setText(tbKhuyenMai.getModel().getValueAt(row, 0).toString());
        txtTenKM.setText(tbKhuyenMai.getModel().getValueAt(row, 1).toString());
        txtNgayBatDau.setText(tbKhuyenMai.getModel().getValueAt(row, 2).toString());
        txtNgayKetThuc.setText(tbKhuyenMai.getModel().getValueAt(row, 3).toString());
        cbTrangThai.setSelectedItem(tbKhuyenMai.getModel().getValueAt(row, 4).toString());
        txtNoiDung.setText(tbKhuyenMai.getModel().getValueAt(row, 5).toString());
    }//GEN-LAST:event_tbKhuyenMaiMouseClicked

