    public void loadData() {

        model.setRowCount(0);
        reponse = service.getAll();
        for (QLKhuyenMaiReponse k : reponse) {
            model.addRow(new Object[]{
                k.getMaKm(), k.getTenKm(), k.getNgayBatDau(), k.getNgayKetThuc(), k.getTrangthai(), k.getNoiDung()
            });
        }
    }

    public QLKhuyenMaiReponse getData() {
        QLKhuyenMaiReponse rp = new QLKhuyenMaiReponse();
        rp.setMaKm(txtMaKM.getText());
        rp.setTenKm(txtTenKM.getText());
        rp.setNgayBatDau(txtNgayBatDau.getText());
        rp.setNgayKetThuc(txtNgayKetThuc.getText());
        rp.setNoiDung(txtNoiDung.getText());
        rp.setTrangthai(cbTrangThai.getSelectedItem().toString());
        return rp;
    }

