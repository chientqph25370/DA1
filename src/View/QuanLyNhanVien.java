    public void clear(){
        txtMa.setText("");
        txtName.setText("");
        txtDiaChi.setText("");
        txtSoGioLam.setText("");
        txtSoNgayLam.setText("");
        txtNgaySinh.setText("");
        txtSDT.setText("");
        buttonGroup1.clearSelection();
        txtSearch.setText("");
        cbbVaiTro.setSelectedItem("Nhân viên");
    }
    public QLNhanVien getFormData(){
        String maNV = txtMa.getText();
        String tenNV = txtName.getText();
        String diaChi = txtDiaChi.getText();
        float soGioLam = Float.parseFloat(txtSoGioLam.getText());
        int soNgayLam = Integer.parseInt(txtSoNgayLam.getText());
        String ngaySinh = txtNgaySinh.getText();
        String sdt = txtSDT.getText();
        String vaiTro = cbbVaiTro.getSelectedItem().toString();
        boolean gioiTinh = rdoNam.isSelected();
        QLNhanVien q = new QLNhanVien(maNV, tenNV, diaChi, ngaySinh, sdt, soGioLam, gioiTinh, vaiTro, soNgayLam);
        return q;
    }
