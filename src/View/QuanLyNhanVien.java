    public void fillTable(int i){
        QLNhanVien q = lists.get(i);
        txtMa.setText(q.getMaNV());
        txtName.setText(q.getTenNV());
        txtDiaChi.setText(q.getDiaChi());
        txtSoGioLam.setText(q.getSoGioLam() + "");
        txtSoNgayLam.setText(q.getSoNgayLam() + "");
        cbbVaiTro.setSelectedItem(q.getVaiTro());
        txtNgaySinh.setText(q.getNgaySinh());
        txtSDT.setText(q.getSdt());
        txtLuongPhaiTra.setText(q.getSoGioLam() * 20.000 * q.getSoNgayLam() + "");
        if(q.isGioiTinh() == true){
            rdoNam.setSelected(true);
        }else{
            rdoNu.setSelected(true);
        }
    }
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        fillTable(row); 
    }//GEN-LAST:event_jTable1MouseClicked

